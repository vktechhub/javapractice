public class CJ_Escape_sequence {
  public static void main(String[] args) {
    System.out.println("Hello World");
    System.out.println("\t - Insert Tab");
    System.out.println("\b - Insert Backspace");
    System.out.println("\n - Insert New line");
    System.out.println("\r - carriage return. ()");
    System.out.println("\' - Insert Single quote");
    System.out.println("\" - Insert Double quote");
    System.out.println("\\ - Insert Backslash");
    System.out.println("\f - form feed");
  }
}
